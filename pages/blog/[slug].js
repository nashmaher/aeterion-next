function renderInline(text) {
  const parts = text.split(/(\*\*[^*]+\*\*)/)
  return parts.map((part, i) =>
    part.startsWith('**') && part.endsWith('**')
      ? <strong key={i}>{part.slice(2, -2)}</strong>
      : part
  )
}

function renderTable(lines, key) {
  const dataRows = lines.filter(r => !r.match(/^\|[\s\-|]+\|$/))
  return (
    <div key={key} style={{ overflowX: 'auto', margin: '24px 0' }}>
      <table style={{ width: '100%', borderCollapse: 'collapse', fontSize: 14 }}>
        <tbody>
          {dataRows.map((row, ri) => {
            const cells = row.split('|').slice(1, -1)
            const isHeader = ri === 0
            return (
              <tr key={ri} style={{ background: isHeader ? '#F8FAFC' : ri % 2 === 0 ? '#fff' : '#F8FAFC' }}>
                {cells.map((cell, ci) =>
                  isHeader
                    ? <th key={ci} style={{ padding: '10px 14px', textAlign: 'left', fontSize: 12, fontWeight: 700, color: '#6B7280', borderBottom: '2px solid #E5E7EB', whiteSpace: 'nowrap' }}>{cell.trim()}</th>
                    : <td key={ci} style={{ padding: '10px 14px', fontSize: 14, color: '#374151', borderBottom: '1px solid #E5E7EB' }}>{cell.trim()}</td>
                )}
              </tr>
            )
          })}
        </tbody>
      </table>
    </div>
  )
}

function renderContent(content) {
  const lines = content.trim().split('\n')
  const elements = []
  let i = 0
  while (i < lines.length) {
    const line = lines[i]
    if (line.trim().startsWith('|')) {
      const tableLines = []
      while (i < lines.length && lines[i].trim().startsWith('|')) { tableLines.push(lines[i]); i++ }
      elements.push(renderTable(tableLines, `table-${i}`))
      continue
    }
    if (line.startsWith('- ')) {
      const items = []
      while (i < lines.length && lines[i].startsWith('- ')) {
        items.push(<li key={i} style={{ fontSize: 15, color: '#374151', lineHeight: 1.75, marginBottom: 6 }}>{renderInline(lines[i].slice(2))}</li>)
        i++
      }
      elements.push(<ul key={`ul-${i}`} style={{ paddingLeft: 24, margin: '12px 0 20px' }}>{items}</ul>)
      continue
    }
    if (line.startsWith('## ')) {
      elements.push(<h2 key={i} style={{ fontSize: 22, fontWeight: 800, color: '#1B3A6B', margin: '36px 0 12px', paddingBottom: 8, borderBottom: '2px solid #EBF5FE' }}>{line.slice(3)}</h2>)
    } else if (line.startsWith('**') && line.endsWith('**') && line.length > 4) {
      elements.push(<p key={i} style={{ fontWeight: 700, fontSize: 16, color: '#1B3A6B', margin: '20px 0 6px' }}>{line.slice(2, -2)}</p>)
    } else if (line.trim() === '') {
      elements.push(<div key={i} style={{ height: 4 }} />)
    } else {
      elements.push(<p key={i} style={{ fontSize: 15, color: '#374151', lineHeight: 1.85, margin: '0 0 16px' }}>{renderInline(line)}</p>)
    }
    i++
  }
  return elements
}

